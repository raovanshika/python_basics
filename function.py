def add(a, b):#function definition ( Function avoids code repetition)
    return a + b #function body

result = add(5, 3) #function call, we are passing 5 and 3 as arguments to the function add, and the result will be stored in the variable result
print(result) #output will be 8, because the function add will return the sum of 5 and 3 which is 8.
